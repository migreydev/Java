package granAlmacen;

public class Cliente {
	 private int numero;
	 private int numeroCaja;

	 public Cliente(int numero, int numeroCaja) {
	        super();
	        this.numero = numero;
	        this.numeroCaja = numeroCaja;
	}

	public int getNumero() {
		return numero;
	}

	public int getNumeroCaja() {
		return numeroCaja;
	}
	 
	 
	 
	 

}
